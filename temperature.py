#celcius to farenheit

def celcius_to_farenheit(x):
    """calculate celcius to farenheit"""
    farenheit = (9*x/5) + 32
    return farenheit

#farenheit to celcius

def farenheit_to_celcius(y):
    """calculate celcius to farenheit"""
    celcius = 1.8*y + 32
    return celcius
